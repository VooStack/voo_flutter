/// Defines the direction for swipe-to-dismiss gesture.
enum VooSwipeDirection {
  /// Swipe down to dismiss (for bottom sheets).
  down,

  /// Swipe up to dismiss.
  up,

  /// Swipe left to dismiss (for side sheets on the right).
  left,

  /// Swipe right to dismiss (for side sheets on the left).
  right,

  /// No swipe gesture allowed.
  none,
}
